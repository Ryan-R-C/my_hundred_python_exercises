# MANAGING PRIMITIVE TYPES

print('Hello! Welcome to Class 04!')
n = input('Type anything!: ')
print(n)
print(type(n))
print('Is {} alphabet?'.format(n), n.isalpha())
print('Is {} a number?'.format(n), n.isnumeric())
print('Is {} a decimal?'.format(n), n.isdecimal())
print('Is {} alphanumeric?'. format(n), n.isalnum())
